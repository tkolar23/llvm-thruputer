#ifndef LLVM_LIB_TARGET_THRU_INSTPRINTER_THRUINSTPRINTER_H
#define LLVM_LIB_TARGET_THRU_INSTPRINTER_THRUINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class ThruInstPrinter : public MCInstPrinter {
public:
  ThruInstPrinter::ThruInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                                   const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  void printOperand(const MCInst *MI, unsigned OpNo, 
                    raw_ostream &O, const char *Modifier = nullptr);

  void printRegName(raw_ostream &OS, unsigned RegNo) const override;

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI);
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address, unsigned OpIdx,
                               unsigned PrintMethodIdx,
                               raw_ostream &OS);

};
} // end namespace llvm

#endif // end LLVM_LIB_TARGET_THRU_INSTPRINTER_THRUINSTPRINTER_H