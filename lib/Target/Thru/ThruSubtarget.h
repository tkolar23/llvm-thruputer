#ifndef LLVM_LIB_TARGET_Thru_ThruSUBTARGET_H
#define LLVM_LIB_TARGET_Thru_ThruSUBTARGET_H

#include "ThruFrameLowering.h"
#include "ThruISelLowering.h"
#include "ThruInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"

#define GET_SUBTARGETINFO_HEADER
#include "ThruGenSubtargetInfo.inc"

namespace llvm {
class ThruSubtarget : public ThruGenSubtargetInfo {
protected:
  SelectionDAGTargetInfo TSInfo;
  ThruInstrInfo InstrInfo;
  ThruFrameLowering FrameLowering;
  ThruTargetLowering TLInfo;
  ThruRegisterInfo RegInfo;
  InstrItineraryData InstrItins;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ThruSubtarget(const Triple &TT, StringRef CPU,
                               StringRef TuneCPU, StringRef FS,
                               StringRef ABIName, const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  ThruSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                  StringRef CPU, StringRef FS,
                                                  const TargetMachine &TM);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const ThruInstrInfo *getInstrInfo() const override {
    return &InstrInfo;
  }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const ThruRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const ThruTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }

  /// getMaxInlineSizeThreshold - Returns the maximum memset / memcpy size
  /// that still makes it profitable to inline the call.
  unsigned getMaxInlineSizeThreshold() const {
    return 64;
  }
};
}

#endif // end LLVM_LIB_TARGET_Thru_ThruSUBTARGET_H